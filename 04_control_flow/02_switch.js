// Switch
// The switch statement executes a block of code depending on different cases.

month = 3;
switch (month) {
    case 1:
        console.log("January");
        break;
    case 2:
        console.log("February");
        break;
    case 3:
        console.log("March");
        break;

    default: console.log("Invalid month");
        break;
} // =>March 